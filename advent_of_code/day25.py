def parse_input(text):
    patterns = text.split("\n\n")
    keys = set()
    locks = set()
    for p in patterns:
        temp = [-1 for _ in range(5)]
        lines = p.split("\n")
        if lines[0] == "#####":
            for l in lines:
                for c in range(len(l)):
                    if l[c] == "#":
                        temp[c] += 1
            locks.add(tuple(temp))
        elif lines[0] == ".....":
            lines = lines[::-1]
            for l in lines:
                for c in range(len(l)):
                    if l[c] == "#":
                        temp[c] += 1
            keys.add(tuple(temp))
            
    return locks, keys


def calculate_fitting_count(text):
    count = 0
    locks, keys = parse_input(text)
    for l in locks:
        for k in keys:
            is_fit = True
            for i in range(5):
                if l[i] + k[i] > 5:
                    is_fit = False
                    break
            if is_fit:
                count += 1
    return count


